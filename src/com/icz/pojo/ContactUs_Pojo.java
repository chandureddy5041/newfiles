package com.icz.pojo;

public class ContactUs_Pojo {
	private int c_id;
	private String c_name, c_emailid, c_msg;

	public int getC_id() {
		return c_id;
	}

	public void setC_id(int c_id) {
		this.c_id = c_id;
	}

	public String getC_name() {
		return c_name;
	}

	public void setC_name(String c_name) {
		this.c_name = c_name;
	}

	public String getC_emailid() {
		return c_emailid;
	}

	public void setC_emailid(String c_emailid) {
		this.c_emailid = c_emailid;
	}

	public String getC_msg() {
		return c_msg;
	}

	public void setC_msg(String c_msg) {
		this.c_msg = c_msg;
	}
}